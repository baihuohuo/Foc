#ifndef __DELAY_H
#define __DELAY_H

#include "sys.h"

// ==============================================================
// 延时函数声明
// 使用 SysTick 定时器实现微秒/毫秒延时
// ==============================================================

// 初始化延时模块
// SYSCLK: 系统时钟频率 (单位: MHz), 如168表示168MHz
void delay_init(u8 SYSCLK);

// 微秒延时
// nus: 延时的微秒数 (范围: 0 ~ 2^32)
void delay_us(u32 nus);

// 毫秒延时
// nms: 延时的毫秒数 (范围: 0 ~ 65535)
void delay_ms(u16 nms);

#endif
